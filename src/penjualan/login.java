    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        try {
            st = con.createStatement();
            rs = st.executeQuery("select*from login where " + "username='" + txt_username.getText() + "'");

            if (rs.next()) {
                if (txt_password.getText().equals(rs.getString("password"))) {
                    JOptionPane.showMessageDialog(rootPane, "Selamat Datang");
                    new produk().setVisible(true);
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Password Salah");
                    txt_password.setText("");
                    txt_password.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Username Salah");
                txt_username.setText("");
                txt_username.requestFocus();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_loginActionPerformed

